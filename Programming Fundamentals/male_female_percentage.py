males = int(input("Please enter the number of males in the class: "))
females = int(input("Please enter the number of females in the class: "))

totalPeople = males + females

malePercentage = males / totalPeople
femalePercentage = females / totalPeople

print("The male percentage is:", malePercentage, "%")
print("The female percentage is:", femalePercentage,"%")